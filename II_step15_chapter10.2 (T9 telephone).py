# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –í–∞—à–∞ –ø—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –∫–∞–∫ –ø—Ä–æ–ø–∏—Å–Ω—ã–µ, —Ç–∞–∫ –∏ —Å—Ç—Ä–æ—á–Ω—ã–µ –±—É–∫–≤—ã.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –í–∞—à–∞ –ø—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –∏–≥–Ω–æ—Ä–∏—Ä–æ–≤–∞—Ç—å –ª—é–±—ã–µ —Å–∏–º–≤–æ–ª—ã, –Ω–µ —É–∫–∞–∑–∞–Ω–Ω—ã–µ –≤ –ø—Ä–∏–≤–µ–¥–µ–Ω–Ω–æ–π –≤—ã—à–µ —Ç–∞–±–ª–∏—Ü–µ.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 3. Nokia 3310, —á—Ç–æ–±—ã –≤—Å–ø–æ–º–Ω–∏—Ç—å, –∫–∞–∫ —ç—Ç–æ –±—ã–ª–æ üòÑ

d = {
    "1": [".", ",", "?", "!", ":"],
    "2": ["A", "B", "C"],
    "3": ["D", "E", "F"],
    "4": ["G", "H", "I"],
    "5": ["J", "K", "L"],
    "6": ["M", "N", "O"],
    "7": ["P", "Q", "R", "S"],
    "8": ["T", "U", "V"],
    "9": ["W", "X", "Y", "Z"],
    "0": [" "]
}

nums = 'Hello, World!'.upper().replace('"', '')

# –ü—Ä–æ–≥–æ–Ω—è–µ–º –≤–æ –≤–Ω–µ—à–Ω–µ–º —Ü–∏–∫–ª–µ —Å—Ç—Ä–æ–∫—É
for i in nums:
    # –ü—Ä–æ–≥–æ–Ω—è–µ–º –≤–æ –≤–Ω—É—Ç—Ä–µ–Ω–Ω–µ–º —Ü–∏–∫–ª–µ –∫–ª—é—á–∏ –∏ –∑–Ω–∞—á–µ–Ω–∏—è —Å–ª–æ–≤–∞—Ä—è
    for key, value in d.items():
        # –ï—Å–ª–∏ —Ç–∞–∫–æ–π —Å–∏–º–≤–æ–ª –ø—Ä–∏—Å—É—Ç—Å–≤—É–µ—Ç –≤ –∑–Ω–∞—á–µ–Ω–∏–∏, –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –µ–≥–æ –≤ —Å–ø–∏—Å–æ–∫
        if i in value:
            x = list(value)
            # –£–º–Ω–æ–∂–∞–µ–º –∫–ª—é—á –Ω–∞ –∏–Ω–¥–µ–∫—Å –∑–Ω–∞—á–µ–Ω–∏—è –∏ –ø—Ä–∏–±–∞–≤–ª—è–µ–º –æ–¥–∏–Ω, —Ç–∞–∫ –∫–∞–∫ –∏–Ω–¥–µ–∫—Å –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å 0
            print(key * (x.index(i) + 1), end='')

